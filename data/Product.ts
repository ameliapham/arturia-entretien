import type { Category } from "./Category";

const allRanges = ["lab", "fuse", "brute", "freak", "step"] as const;

export type Range = (typeof allRanges)[number];

export type Product = {
  name: string;
  description: string;
  price: number | undefined;
  productCode: string;
  range: Range;
  categories: Category[];
  tag: "featured" | "new" | undefined;
  size: "big" | "small";
};

export function getFilteredProductsByRange(params: {
  categories: readonly Category[];
}): Record<Range, Product[]> {
  const { categories } = params;

  const filteredProductsByRange: Record<Range, Product[]> = {
    lab: [],
    fuse: [],
    brute: [],
    freak: [],
    step: [],
  };

  for (const product of products) {
    if (!product.categories.some((category) => categories.includes(category))) {
      continue;
    }

    filteredProductsByRange[product.range].push(product);
  }

  for (const range of allRanges) {
    filteredProductsByRange[range].sort((a, b) => {
      const priority = { featured: 2, new: 1, undefined: 0 };
      return priority[`${b.tag}`] - priority[`${a.tag}`];
    });
  }

  return filteredProductsByRange;
}

